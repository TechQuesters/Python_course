'''
Basic addition (+) in python

'''

a=50 # variable a with integral value 50
b=10 # variable b with integral value 10

print(a+b) # printing the sum a+b (50+10)=60

# or we can assign the sum to a 3rd variable and print it
sum=a+b
print(sum) # same output as a+b i.e 60

# Taking values from user
num1=int(input("Enter a 1st number : ")) # taking 1st number from user
num2=int(input("Enter a 1st number : ")) # taking 2nd number from user

sum=num1+num2 # adds both the numbers
print(sum)