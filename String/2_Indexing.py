# lets learn about indexing

'''
NOTE :
    --> Every letter in a String has its own index/location
    --> Indexing starts from 0 to (n-1) where n is the string_length
    --> Negative indexing is also used -n to -1
'''

name="python" # here n=6 so indixing is like(0,1,2,3,4,5)

print(name[0]) # square brackets is used to access each letter or string members
print(name[1])
print(name[2])
print(name[3])
print(name[4])
print(name[5])

'''
    ---> We see that each element is extracted succesfully
    ---> we can also do the same by Negative indexing
'''

print(name[-6]) # square brackets is used to access each letter or string members
print(name[-5])
print(name[-4])
print(name[-3])
print(name[-2])
print(name[-1])